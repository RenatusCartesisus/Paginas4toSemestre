console.log(Math);

// Numeros PI y EULER
console.log(Math.PI);
console.log(Math.E);

// Valor absoluto = abs
console.log(Math.abs(-7.8));
// Redondea un numero a su entero MAYOR = ceil
console.log(Math.ceil(7.8));
// Redondea un numero a su entero MENOR = floor
console.log(Math.floor(7.8));
// Redondea un numero a su entero mas cercano independientemente de menor o mayor = round
console.log(Math.round(7.2));
// Raiz cuadrada de un numero = sqrt
console.log(Math.sqrt(81));
// Eleva el primer valor a la potencia del segundo valor = pow
console.log(Math.pow(2,5));
// Indica si el numero es negativo o positivo = sign
console.log(Math.sign(7)); 
// Regresa un numero aleatorio entre 0 y 1 = random()
console.log(Math.random());

// Obtienes un numero entre 0 y el valor que indicas 
console.log(Math.round(Math.random()*1000));
// Encuentra el menor valor de un conjunto de datos = min
console.log(Math.min(1,2,3,4,5,6,7));
// Encuentra el mayor valor de un conjunto de datos = max
console.log(Math.max(1,2,3,4,5,6,7));

console.log(Math.cos(Math.PI));

console.log(Math.asin(Math.PI/12));
